package z_doit_java;

import java.util.Scanner;

class T08_04 {
	static int bmMatch(String txt, String pat) {
		int pt;
		int pp;
		int[] skip = new int[Character.MAX_VALUE + 1];	//건너뛰기 표
		
		//건너뛰기 표 만들기
		for(pt = 0; pt <= Character.MAX_VALUE; pt++) skip[pt] = pat.length();
		for(pt = 0; pt < pat.length()-1; pt++) skip[pat.charAt(pt)] = pat.length()-pt-1;
		
		//검색
		while(pt < txt.length()) {
			pp = pat.length()-1;
			
			while(txt.charAt(pt) == pat.charAt(pp)) {
				if(pp == 0) return pt;	//검색 성공
				pp--; pt--;
			}
			pt += (skip[txt.charAt(pt)] > pat.length()-pp) ? skip[txt.charAt(pt)] : pat.length()-pp;
		}
		return -1;	//검색 실패
	}
	
    public static void main(String[] args) {
        Scanner kb = new Scanner(System.in);
        System.out.print("텍스트 : ");
        String s1 = kb.next();
        System.out.print("패턴 : ");
        String s2 = kb.next();

        int idx = bmMatch(s1, s2);

        if (idx == -1) System.out.println("텍스트 안에 패턴이 없습니다.");
        else {
            //일치하는 문자 바로 앞까지 반각(1바이트)문자의 개수를 구함
            int len = 0;
            for (int i = 0; i < idx; i++)
                len += s1.substring(i, i + 1).getBytes().length;
            len += s2.length();

            System.out.println((idx + 1) + "번째 문자부터 일치합니다.");
            System.out.println("텍스트 : " + s1);
            System.out.printf(String.format("패턴 : %%%ds\n", len), s2);
        }
    }
}
